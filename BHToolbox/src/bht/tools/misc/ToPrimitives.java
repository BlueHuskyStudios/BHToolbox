package bht.tools.misc;

/**
 *
 * @author Supuhstar
 */
public interface ToPrimitives
{
  /**
   * Returns a <tt>boolean</tt> representation of this object.
   * @return a <tt>boolean</tt> representation of this object.
   */
  public boolean toBoolean();
  /**
   * Returns a <tt>byte</tt> representation of this object.
   * @return a <tt>byte</tt> representation of this object.
   */
  public byte toByte();
  /**
   * Returns a <tt>char</tt> representation of this object.
   * @return a <tt>char</tt> representation of this object.
   */
  public char toChar();
  /**
   * Returns a <tt>double</tt> representation of this object.
   * @return a <tt>double</tt> representation of this object.
   */
  public double toDouble();
  /**
   * Returns a <tt>float</tt> representation of this object.
   * @return a <tt>float</tt> representation of this object.
   */
  public float toFloat();
  /**
   * Returns a <tt>int</tt> representation of this object.
   * @return a <tt>int</tt> representation of this object.
   */
  public int toInt();
  /**
   * Returns a <tt>long</tt> representation of this object.
   * @return a <tt>long</tt> representation of this object.
   */
  public long toLong();
  /**
   * Returns a <tt>short</tt> representation of this object.
   * @return a <tt>short</tt> representation of this object.
   */
  public short toShort();
}
